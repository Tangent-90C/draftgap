const dictionaries = {
    en_US: en,
    zh_CN: zh,
} as const;

export type I18nKey = keyof typeof en;

export function t(
    configOrLanguage: DraftGapConfig | string,
    key: I18nKey,
    values: Record<string, string | number> = {},
) {
    const language =
        typeof configOrLanguage === "string"
            ? configOrLanguage
            : configOrLanguage.language;
    const dictionary =
        dictionaries[language as keyof typeof dictionaries] ?? dictionaries.en_US;
    const template = dictionary[key] ?? dictionaries.en_US[key];

    return template.replace(/\{(\w+)\}/g, (_, name) =>
        values[name] === undefined ? `{${name}}` : String(values[name]),
    );
}

export function teamName(config: DraftGapConfig, team: "ally" | "opponent") {
    return t(config, team);
}

export function roleName(config: DraftGapConfig, role: Role) {
    return {
        [Role.Top]: t(config, "top"),
        [Role.Jungle]: t(config, "jungle"),
        [Role.Middle]: t(config, "middle"),
        [Role.Bottom]: t(config, "bot"),
        [Role.Support]: t(config, "support"),
    }[role];
}

export function riskLevelName(config: DraftGapConfig, riskLevel: RiskLevel) {
    return {
        "very-low": t(config, "veryLow"),
        low: t(config, "low"),
        medium: t(config, "medium"),
        high: t(config, "high"),
        "very-high": t(config, "veryHigh"),
    }[riskLevel];
}
